#!/usr/bin/env bash
# =============================================================================
# generate-reports.sh — Generate regression test reports
#
# Usage: bash generate-reports.sh <reports_dir>
#
# Reads per-version result directories under <reports_dir>/php-X.Y/ and
# generates three Markdown reports:
#   1. compatibility-report.md   — PHP version compatibility matrix
#   2. developer-fix-report.md   — Deduplicated errors/warnings with fix suggestions
#   3. coverage-report.md        — Code coverage per file, sorted, with highlights
# =============================================================================

set -euo pipefail

REPORTS_DIR="${1:?Usage: generate-reports.sh <reports_dir>}"

# Timestamp for reports
REPORT_DATE=$(date "+%Y-%m-%d %H:%M:%S")

# Discover tested versions (sorted)
VERSIONS=()
for dir in "${REPORTS_DIR}"/php-*/; do
    if [ -d "${dir}" ]; then
        ver=$(basename "${dir}" | sed 's/^php-//')
        VERSIONS+=("${ver}")
    fi
done

# Sort versions
IFS=$'\n' VERSIONS=($(sort -V <<<"${VERSIONS[*]}")); unset IFS

if [ ${#VERSIONS[@]} -eq 0 ]; then
    echo "ERROR: No version result directories found in ${REPORTS_DIR}"
    exit 1
fi

echo "Generating reports for PHP versions: ${VERSIONS[*]}"

# =============================================================================
# Helper: read a field from summary.json (simple grep-based JSON parser)
# =============================================================================
read_json_field() {
    local file="$1"
    local field="$2"
    if [ -f "${file}" ]; then
        grep -o "\"${field}\": *\"\\{0,1\\}[^\",}]*" "${file}" | head -1 | sed 's/.*: *"\{0,1\}//' | sed 's/"\{0,1\}$//'
    else
        echo ""
    fi
}

read_json_int() {
    local file="$1"
    local field="$2"
    local val
    val=$(read_json_field "${file}" "${field}")
    if [[ "${val}" =~ ^-?[0-9]+$ ]]; then
        echo "${val}"
    else
        echo "0"
    fi
}

# =============================================================================
# REPORT 1: Compatibility Report
# =============================================================================
generate_compatibility_report() {
    local output="${REPORTS_DIR}/compatibility-report.md"
    echo "  Generating compatibility report ..."

    cat > "${output}" <<'HEADER'
# PHP Version Compatibility Report

> Auto-generated by the PHP SDK regression test suite.

## Compatibility Matrix

HEADER

    echo "| PHP Version | Composer Install | Tests Run | Tests Passed | Tests Failed | Tests Errors | Tests Skipped | Status |" >> "${output}"
    echo "|:-----------:|:----------------:|:---------:|:------------:|:------------:|:------------:|:-------------:|:------:|" >> "${output}"

    local compatible_versions=""
    local partial_versions=""
    local incompatible_versions=""

    for ver in "${VERSIONS[@]}"; do
        local summary="${REPORTS_DIR}/php-${ver}/summary.json"
        local composer_install
        local tests_run tests_passed tests_failed tests_errors tests_skipped
        local status status_icon composer_icon

        composer_install=$(read_json_field "${summary}" "composer_install")
        tests_run=$(read_json_int "${summary}" "tests_run")
        tests_passed=$(read_json_int "${summary}" "tests_passed")
        tests_failed=$(read_json_int "${summary}" "tests_failed")
        tests_errors=$(read_json_int "${summary}" "tests_errors")
        tests_skipped=$(read_json_int "${summary}" "tests_skipped")
        status=$(read_json_field "${summary}" "status")

        # Composer icon
        case "${composer_install}" in
            pass)   composer_icon="✅ Pass" ;;
            fail)   composer_icon="❌ Fail" ;;
            *)      composer_icon="❓ N/A" ;;
        esac

        # Status icon
        case "${status}" in
            compatible)
                status_icon="✅ Compatible"
                compatible_versions="${compatible_versions} ${ver}"
                ;;
            partial)
                status_icon="⚠️ Partial"
                partial_versions="${partial_versions} ${ver}"
                ;;
            incompatible)
                status_icon="❌ Incompatible"
                incompatible_versions="${incompatible_versions} ${ver}"
                ;;
            build_fail)
                status_icon="❌ Build Failed"
                incompatible_versions="${incompatible_versions} ${ver}"
                ;;
            error)
                status_icon="❌ Error"
                incompatible_versions="${incompatible_versions} ${ver}"
                ;;
            *)
                status_icon="❓ Unknown"
                incompatible_versions="${incompatible_versions} ${ver}"
                ;;
        esac

        # Tests display
        local tests_run_display="${tests_run}"
        local tests_passed_display="${tests_passed}"
        local tests_failed_display="${tests_failed}"
        local tests_errors_display="${tests_errors}"
        local tests_skipped_display="${tests_skipped}"

        if [ "${composer_install}" = "fail" ] || [ "${status}" = "build_fail" ]; then
            tests_run_display="-"
            tests_passed_display="-"
            tests_failed_display="-"
            tests_errors_display="-"
            tests_skipped_display="-"
        fi

        echo "| ${ver} | ${composer_icon} | ${tests_run_display} | ${tests_passed_display} | ${tests_failed_display} | ${tests_errors_display} | ${tests_skipped_display} | ${status_icon} |" >> "${output}"
    done

    # Summary section
    cat >> "${output}" <<EOF

## Summary

- **Report generated**: ${REPORT_DATE}
- **Versions tested**: ${#VERSIONS[@]}
EOF

    if [ -n "${compatible_versions}" ]; then
        echo "- **Compatible**:${compatible_versions}" >> "${output}"
    fi
    if [ -n "${partial_versions}" ]; then
        echo "- **Partially compatible**:${partial_versions}" >> "${output}"
    fi
    if [ -n "${incompatible_versions}" ]; then
        echo "- **Incompatible**:${incompatible_versions}" >> "${output}"
    fi

    # Recommended minimum version
    local min_compatible=""
    for ver in "${VERSIONS[@]}"; do
        local s
        s=$(read_json_field "${REPORTS_DIR}/php-${ver}/summary.json" "status")
        if [ "${s}" = "compatible" ] && [ -z "${min_compatible}" ]; then
            min_compatible="${ver}"
        fi
    done

    if [ -n "${min_compatible}" ]; then
        cat >> "${output}" <<EOF

## Recommendation

Based on the test results, the minimum compatible PHP version is **${min_compatible}**.

\`\`\`json
"require": {
    "php": ">=${min_compatible}"
}
\`\`\`
EOF
    fi

    echo "" >> "${output}"
    echo "---" >> "${output}"
    echo "*Generated by [PHP SDK Regression Test Suite](regression/README.md)*" >> "${output}"
}

